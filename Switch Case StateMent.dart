void main() {
  String vehicle = 'Car';
  switch (vehicle) {
    case 'Car':
      print("This is a car");
      break;
    case 'Truck':
      print("This is a Truck");
      break;
    case 'Bus':
      print("This is a Bus");
      break;
    default:
      print("This is not a vehicle");
  }
}
