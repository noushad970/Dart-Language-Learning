void main() {
  /*
  final and const keyword are use to make a variable constant.that mean we can not change the value
  final keyword wont take any memory space untill we use it on print function;
  but const will take a memory space when it will be declare 
  we cannot use const keyword directly in a class
  */
  final a = 10;
  const b = 10.5;
  print(a + b);
}

